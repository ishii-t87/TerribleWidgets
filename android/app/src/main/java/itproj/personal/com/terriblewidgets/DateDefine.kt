package itproj.personal.com.terriblewidgets

/**
 *
 * @brief 日付を定義する構造体
 *
 * */
class DateDefine(year: Int = 1970, month: Int = 1, date: Int = 1, hourOfDay: Int = 0, minute: Int = 0) {
    val YEAR: Int = year
    val MONTH: Int = month
    val DATE: Int = date
    val HOUR_OF_DAY: Int = hourOfDay
    val MINUTE: Int = minute
}
